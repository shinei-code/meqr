<script>
export default {
  data() {
    return {
      patient: {}, // store情報の入れ物
      mj101: {}, // store情報の入れ物
      sofusaki: null,
      shogaiNenkin: [],
      sikyumotoNenkin: [],
      sasidasinin: null,
      sinseiHouhou: null,

      // 幅調整
      wAdjust: {
        hihoName: null,
        hihoKana: null,
        hihoBday: null,
        sasidasinin: null,
      }
    }
  },
  props: {
    sinseiNaiyou: {
      required: true,
      default: null
    },
    documentId: {
      required: true,
      default: null
    }
  },
  mounted() {
    this.mj101 = this.$store.getters.getMj101;
    this.patient = this.$store.getters.getPatient;

    // 障がい年金等
    for (let index = 1; index < 8; index++) {
      if (this.mj101['is_shogai_nenkin' + index] == '1') {
        this.shogaiNenkin.push(this.$getLabel(this.$MJ101.is_shogai_nenkin, index))
      }
    }
    this.shogaiNenkin = this.shogaiNenkin.join(", ")

    // 年金支給元
    for (let index = 1; index < 7; index++) {
      if (this.mj101['is_nenkin_sikyumoto' + index] == '1') {
        this.sikyumotoNenkin.push(this.$getLabel(this.$COMMON.is_nenkin_sikyumoto, index))
      }
    }
    this.sikyumotoNenkin = this.sikyumotoNenkin.join(", ")

    // 申請方法
    if ([1, 4].includes(this.mj101.ssti_sinseinaiyou_kbn_key)) {  // 新規, 有効期限切れ
      this.sinseiHouhou = this.$getLabel(this.$MJ101.ssti_sinseihouhou_kbn_key, this.mj101.ssti_sinseihouhou_kbn_key);
    } else if(this.mj101.ssti_sinseinaiyou_kbn_key == 2) {  // 再認定_有効期限内
      if (this.mj101.is_sindansho_umu) {
        this.sinseiHouhou = "診断書あり";
      } else {
        this.sinseiHouhou = "診断書なし";
      }
    } else if(this.mj101.ssti_sinseinaiyou_kbn_key == 3) {  // 再認定_手帳同時申請
      this.sinseiHouhou = "手帳用診断書";
    } else {
        this.sinseiHouhou = "";
    }

    // 差出人
    this.sasidasinin = this.$getQrVisitor('visitor') + ' 受付者:'

    // 送付先
    this.sofusaki = this.$getLabel(this.$COMMON.SOFUSAKI_KBN, this.$store.getters.getMj101.sofusakiKbn);
    const sofusakiIryoKikan = this.$getQrSofusaki(this.mj101, 'sofusaki');
    if (sofusakiIryoKikan) {
      this.sofusaki += ' : ' + sofusakiIryoKikan;
    }

    // 幅調整
    this.wAdjust.hihoName = this.$wAdjustAttr(this.$getHihoName(), 400);
    this.wAdjust.hihoKana = this.$wAdjustAttr(this.$getHihoKana(), 400);
    this.wAdjust.hihoBday = this.$wAdjustAttr(this.$getHihoBday(false), 300);
    this.wAdjust.sasidasinin = this.$wAdjustAttr(this.sasidasinin, 800);
  }
}
