export default {
  data() {
    return {
      patient: {}, // store情報の入れ物
      mj101: {}, // store情報の入れ物
      iryokikan: {},
      hokenja: null,
      notes: null,
      iryoKikan: {
        hospital1: {
          name: null,
          addr: null,
          tel: null
        },
        yakkyoku: {
          name: null,
          addr: null,
          tel: null
        },
        houkan: {
          name: null,
          addr: null,
          tel: null
        },
        hospital2: {
          name: null,
          addr: null,
          tel: null
        },
      },
      wAdjust: {
        ptAddr: null,
        hospital1Name: null,
        yakkyokuName: null,
        houkanName: null,
        hospital2Name: null,
      }
    }
  },
  props: {
    qrcode: {
      required: true
    },
    sinseiNaiyou: {
      required: true,
      default: null
    },
    documentId: {
      required: true,
      default: "MJ_101"
    }
  },
  mounted() {
    this.mj101 = this.$store.getters.getMj101;
    this.patient = this.$store.getters.getPatient;

    // 医療機関
    if (this.iryoKikan.hospital1) {
      this.iryoKikan.hospital1.name = this.$getHospital1('name');
      this.iryoKikan.hospital1.addr = this.$getHospital1('address');
      this.iryoKikan.hospital1.tel = this.$getHospital1('tel');
    }
    if (this.iryoKikan.yakkyoku) {
      this.iryoKikan.yakkyoku.name = this.$getYakkyoku('name');
      this.iryoKikan.yakkyoku.addr = this.$getYakkyoku('address');
      this.iryoKikan.yakkyoku.tel = this.$getYakkyoku('tel');
    }
    if (this.iryoKikan.houkan) {
      this.iryoKikan.houkan.name = this.$getHoukan('name');
      this.iryoKikan.houkan.addr = this.$getHoukan('address');
      this.iryoKikan.houkan.tel = this.$getHoukan('tel');
    }
    if (this.iryoKikan.hospital2) {
      this.iryoKikan.hospital2.name = this.$getHospital2('name');
      this.iryoKikan.hospital2.addr = this.$getHospital2('address');
      this.iryoKikan.hospital2.tel = this.$getHospital2('tel');
    }

    // 保険者名
    this.hokenja = '';
    if (['11', '12'].includes(this.patient.hokenType)) {
      this.hokenja = this.$getLabel(this.$COMMON.HOKEN_TYPE, this.patient.hokenType);
    }
    if (this.patient.hokenType != '11') {
      this.hokenja = this.$getHokenja('name');
    }

    // 備考
    if (this.mj101.ssti_sinseinaiyou_kbn_key == '4') {  // 有効期限切れ
      this.notes = `自立期限： ${this.$getIryoExp(false)}`;

      const kisaiJikoHenko = []
      for (let index = 1; index < 5; index++){
        if (this.mj101['kisaiJikoHenko'+index]) {
          kisaiJikoHenko.push(this.$getLabel(this.$MJ101.kisaiJikoHenko, index))
        }
      }
      this.notes += kisaiJikoHenko.length ? `　　再登録時より変更：${kisaiJikoHenko.join(", ")}` : '';

    } else if (this.mj101.is_kikan_chosei == '1') {  // 期間調整する
      this.notes = `期間調整希望　　自立期限： ${this.$getIryoExp(false)}`;
    }

    // 幅調整
    this.wAdjust.ptAddr = this.$wAdjustAttr(this.$getPtAddr(), 900);
    this.wAdjust.hospital1Name = this.$wAdjustAttr(this.$getHospital1('name'), 800);
    this.wAdjust.yakkyokuName = this.$wAdjustAttr(this.$getYakkyoku('name'), 800);
    this.wAdjust.houkanName = this.$wAdjustAttr(this.$getHoukan('name'), 800);
    this.wAdjust.hospital2Name = this.$wAdjustAttr(this.$getHospital2('name'), 800);
  },
}
